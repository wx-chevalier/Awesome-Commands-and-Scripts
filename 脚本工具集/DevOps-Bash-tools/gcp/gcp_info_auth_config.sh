set -euo pipefail
[ -n "${DEBUG:-}" ] && set -x
srcdir="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# shellcheck disable=SC1090,SC1091
. "$srcdir/lib/utils.sh"

# shellcheck disable=SC1090,SC1091
. "$srcdir/lib/gcp.sh"

# shellcheck disable=SC2034,SC2154
usage_description="
Lists GCP Auth, Organizations and Config

Can optionally specify a project id using the first argument, otherwise uses currently configured project

$gcp_info_formatting_help
"

# used by usage() in lib/utils.sh
# shellcheck disable=SC2034
usage_args="[<project_id>]"

help_usage "$@"

check_bin gcloud

if [ $# -gt 0 ]; then
    project_id="$1"
    shift || :
    export CLOUDSDK_CORE_PROJECT="$project_id"
fi


# Organizations
cat <<EOF
# ============================================================================ #
#                           O r g a n i z a t i o n s
# ============================================================================ #

EOF

gcp_info "Organizations" gcloud organizations list


# Auth & Config
cat <<EOF


# ============================================================================ #
#                           A u t h   &   C o n f i g
# ============================================================================ #

EOF

gcp_info "GCloud SDK Configurations" gcloud config configurations list
echo

# list credentialed accounts and show which one is active - dupliates info from configurations so not needed
#gcloud auth list
#echo

echo "========================="
echo "GCloud SDK Configuration:"
echo
gcloud config list
